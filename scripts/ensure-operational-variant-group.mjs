#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { randomUUID } from 'node:crypto';
import { spawnSync } from 'node:child_process';
import { createClient } from '@supabase/supabase-js';

const rawArgs = process.argv.slice(2);
const args = new Set(rawArgs);
const json = args.has('--json');
const apply = args.has('--apply');
const fromVercel = args.has('--from-vercel');

function argValue(name, fallback = '') {
  const prefix = `${name}=`;
  const inline = rawArgs.find((arg) => arg.startsWith(prefix));
  if (inline) return inline.slice(prefix.length);
  const index = rawArgs.indexOf(name);
  return index >= 0 ? rawArgs[index + 1] ?? fallback : fallback;
}

function writeReport(report, exitCode = 0) {
  if (json) console.log(JSON.stringify(report, null, 2));
  else {
    console.log(`Operational variant group ${report.status}.`);
    if (report.variant_group_id) console.log(`MARKETING_CHECK_VARIANT_GROUP_ID=${report.variant_group_id}`);
    if (report.reason) console.log(report.reason);
  }
  process.exitCode = exitCode;
}

function fail(message, details = {}) {
  writeReport({ status: 'fail', applied: false, error: message, ...details }, 1);
}

function parseEnvLine(line) {
  const trimmed = String(line || '').trim();
  if (!trimmed || trimmed.startsWith('#')) return null;
  const normalized = trimmed.startsWith('export ') ? trimmed.slice(7).trimStart() : trimmed;
  const equalIndex = normalized.indexOf('=');
  if (equalIndex <= 0) return null;
  const key = normalized.slice(0, equalIndex).trim();
  if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) return null;
  let value = normalized.slice(equalIndex + 1).trim();
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    value = value.slice(1, -1);
  }
  return [
    key,
    value
      .replace(/\\n/g, '\n')
      .replace(/\\r/g, '\r')
      .replace(/\\"/g, '"'),
  ];
}

function loadEnvFile(path) {
  if (!path) return [];
  if (!existsSync(path)) fail(`Env file not found: ${path}`);
  const loaded = [];
  for (const line of readFileSync(path, 'utf8').split(/\r?\n/)) {
    const entry = parseEnvLine(line);
    if (!entry) continue;
    const [key, value] = entry;
    if (!String(process.env[key] || '').trim()) process.env[key] = value;
    loaded.push(key);
  }
  return [...new Set(loaded)].sort();
}

function envValue(...keys) {
  for (const key of keys) {
    const value = process.env[key];
    if (typeof value === 'string' && value.trim()) return value.trim();
  }
  return '';
}

function run(command, commandArgs, options = {}) {
  return spawnSync(command, commandArgs, {
    cwd: process.cwd(),
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
    windowsHide: true,
    ...options,
  });
}

function runVercelEnvPull(path, environment, timeoutMs) {
  if (process.platform === 'win32') {
    return run('cmd.exe', [
      '/d',
      '/s',
      '/c',
      `npx vercel env pull ${path} --yes --environment=${environment}`,
    ], { timeout: timeoutMs });
  }
  return run('npx', ['vercel', 'env', 'pull', path, '--yes', `--environment=${environment}`], {
    timeout: timeoutMs,
  });
}

function ensureParent(path) {
  mkdirSync(dirname(resolve(path)), { recursive: true });
}

function writeOutFile(path, groupId, source) {
  if (!path) return;
  ensureParent(path);
  writeFileSync(path, [
    '# Generated by scripts/ensure-operational-variant-group.mjs',
    '# Contains a non-secret readiness probe identifier.',
    `# source: ${source}`,
    `MARKETING_CHECK_VARIANT_GROUP_ID=${groupId}`,
    '',
  ].join('\n'));
}

async function main() {
  const outPath = argValue('--out', '.tmp/operational-variant-group.env');
  const environment = argValue('--environment', process.env.OPERATIONAL_DISCOVERY_VERCEL_ENV || 'production');
  const pullTimeoutMs = Number(argValue('--pull-timeout-ms', process.env.OPERATIONAL_DISCOVERY_VERCEL_PULL_TIMEOUT_MS || '30000'));
  const tempEnvPath = join('.tmp', `operational-variant-vercel-env-${process.pid}.env`);
  let envFile = argValue('--env-file', process.env.OPERATIONAL_INPUTS_ENV_FILE || '');
  let pulledTemp = false;

  if (fromVercel) {
    if (!Number.isFinite(pullTimeoutMs) || pullTimeoutMs <= 0) {
      fail('--pull-timeout-ms must be a positive number of milliseconds.');
      return;
    }
    const pull = runVercelEnvPull(tempEnvPath, environment, pullTimeoutMs);
    if (pull.status !== 0 || pull.error) {
      fail('Could not pull Vercel environment variables.', {
        environment,
        timedOut: pull.error?.code === 'ETIMEDOUT',
        detail: String(pull.stderr || pull.stdout || pull.error?.message || 'unknown error').trim().slice(0, 500),
      });
      return;
    }
    envFile = tempEnvPath;
    pulledTemp = true;
  }

  try {
    const loadedEnvFileKeys = loadEnvFile(envFile);
    const supabaseUrl = envValue('SUPABASE_URL', 'NEXT_PUBLIC_SUPABASE_URL');
    const serviceKey = envValue('SUPABASE_SERVICE_ROLE_KEY', 'SUPABASE_SERVICE_KEY');
    const missingConnection = [
      !supabaseUrl ? 'SUPABASE_URL or NEXT_PUBLIC_SUPABASE_URL' : '',
      !serviceKey ? 'SUPABASE_SERVICE_ROLE_KEY or SUPABASE_SERVICE_KEY' : '',
    ].filter(Boolean);
    if (missingConnection.length > 0) {
      fail('Supabase connection is not available.', { missingConnection, loadedEnvFileKeys });
      return;
    }

    const supabase = createClient(supabaseUrl, serviceKey, {
      auth: { persistSession: false },
      global: { headers: { 'x-application-name': 'operational-variant-group-ensure' } },
    });

    const { data: existing, error: existingErr } = await supabase
      .from('card_news')
      .select('variant_group_id')
      .not('variant_group_id', 'is', null)
      .order('updated_at', { ascending: false, nullsFirst: false })
      .limit(1);
    if (existingErr) {
      fail('Could not inspect existing variant groups.', { error: existingErr.message });
      return;
    }
    const existingGroupId = String(existing?.[0]?.variant_group_id || '').trim();
    if (existingGroupId) {
      writeOutFile(outPath, existingGroupId, 'card_news.variant_group_id');
      writeReport({
        status: 'pass',
        applied: false,
        variant_group_id: existingGroupId,
        source: 'existing',
        outPath,
        loadedEnvFileKeys,
      });
      return;
    }

    const variantGroupId = randomUUID();
    if (!apply) {
      writeReport({
        status: 'blocked',
        applied: false,
        reason: 'No card_news.variant_group_id row exists. Re-run with --apply to create a DRAFT readiness-only variant group.',
        plannedRows: 2,
        outPath,
        loadedEnvFileKeys,
      }, 1);
      return;
    }

    const now = new Date().toISOString();
    const rows = [
      {
        title: '[READINESS] Variant smoke baseline',
        status: 'DRAFT',
        card_news_type: 'info',
        slides: [],
        template_family: 'editorial',
        template_version: 'readiness-smoke',
        variant_group_id: variantGroupId,
        variant_angle: 'baseline',
        variant_score: 72,
        variant_score_detail: { summary: 'Operational readiness smoke row', generatedAt: now },
        generation_config: { operational_readiness: true, created_by: 'ensure-operational-variant-group.mjs' },
      },
      {
        title: '[READINESS] Variant smoke value angle',
        status: 'DRAFT',
        card_news_type: 'info',
        slides: [],
        template_family: 'bold',
        template_version: 'readiness-smoke',
        variant_group_id: variantGroupId,
        variant_angle: 'value',
        variant_score: 69,
        variant_score_detail: { summary: 'Operational readiness smoke row', generatedAt: now },
        generation_config: { operational_readiness: true, created_by: 'ensure-operational-variant-group.mjs' },
      },
    ];
    const { data: inserted, error: insertErr } = await supabase
      .from('card_news')
      .insert(rows)
      .select('id');
    if (insertErr) {
      fail('Could not create DRAFT readiness variant group.', { error: insertErr.message });
      return;
    }

    writeOutFile(outPath, variantGroupId, 'created-readiness-draft-card-news');
    writeReport({
      status: 'pass',
      applied: true,
      variant_group_id: variantGroupId,
      insertedCount: Array.isArray(inserted) ? inserted.length : 0,
      outPath,
      loadedEnvFileKeys,
    });
  } finally {
    if (pulledTemp) rmSync(tempEnvPath, { force: true });
  }
}

await main();
