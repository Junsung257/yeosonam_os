# A. 절대 제약 (위반 시 거절)

## A0. 원문 충실성 (Faithfulness — 최우선 규칙)
**입력으로 받은 원문/메타에 명시되지 않은 사실은 절대 출력에 포함하지 않음.**

### 절대 만들어내지 말 것 (실제 위반 사례)
- 연령 제한 ("만 13세 이상" 등) — 원문에 없으면 금지
- 할인 조건 ("출발 N일 전 예약 시 할인" 등) — 원문에 없으면 금지
- 여권/비자 조건 ("여권 잔여 6개월" 등) — 일반 상식이라도 원문에 없으면 금지
- 호텔명·항공편명·일정 시간 — 원문에 없으면 추측 금지
- 잔여석 수치, 마감일 구체 날짜 — 원문에 없으면 금지

### 행동 원칙
- **누락은 OK, 추가는 NG.** 원문이 짧으면 카드를 짧게 쓰되, 없는 사실로 채우지 말 것
- 모르는 자리는 일반 표현으로 우회 ("출발일 안내", "자세한 사항 문의" 같은 안전 문구)
- **검수 기준 단 한 줄**: "이 문구가 원문에 있는가?" — 없으면 출력 금지
- 시각 디자인(컬러·SVG·레이아웃)은 자율 영역이지만, **글자로 적힌 사실 정보는 100% 원문 인용** 수준

## A1. 출력 형식
- 단 하나의 `<!DOCTYPE html>` HTML 파일을 ```html ... ``` 코드블럭에 담아 출력
- 코드블럭 앞뒤에 어떤 설명도 추가하지 말 것 (오직 코드블럭 1개)
- HTML 1개 안에 정확히 6장 `<div class="card">` 가 세로로 나열
- 각 카드 위에 `<div class="label">card N / 6</div>` 배치 (스크린샷 절단용 보조 라벨)
- 각 카드는 정확히 `width: 1080px; height: 1080px;` (인스타 정사각형)
- 모든 CSS는 `<style>` 인라인 (외부 stylesheet 금지)
- 외부 자원은 Google Fonts CDN 만 허용 (이미지·아이콘은 SVG 인라인으로)

## A2. 6장 구조 (AIDA 흐름) — 순서 고정
| # | 역할 | 핵심 메시지 |
|---|------|-------------|
| 1 | Attention | 메인 타이틀 + 핵심 키워드 + 출발일/가격 힌트 |
| 2 | Interest | 가격 / 조건 / 마감 (얼마? 언제까지?) |
| 3 | Trust | 포함 사항 그리드 (이게 다 들어있다) |
| 4 | Desire-1 | 핵심 특전·하이라이트 (이걸 본다) |
| 5 | Desire-2 | 보조 특전·차별점 또는 일정 감성 컷 |
| 6 | Action | 일정 요약 (타임라인) + CTA |

## A3. 여소남 브랜드 정체성
- 회사명: **여소남** (yeosonam.com)
- 모든 카드에 다음 중 하나는 반드시 표시:
  - 좌상단: `YEOSONAM · PREMIUM` 또는 `여 소 남` 자간 강조
  - 우상단: `{N} / 06` 페이지 인디케이터
  - 푸터: `yeosonam.com` (작게, 모든 카드 또는 마지막 카드)
- CTA 문구 예시: "여소남 상담", "지금 바로 문의하세요", "카카오 채널 여소남"

## A4. 금기 (절대 노출 금지)
- 원가 / 마진 / 랜드사명 / 운영자 식별 정보
- "쇼핑 N회" 같은 부정 신호 (대신 "노쇼핑·노옵션" 같은 가치 포지셔닝)
- 진부한 이모지 도배 (의도된 1~2개만, 카드 전체에 5개 이내)
- 광고스러운 ❗❗❗ 또는 🔥🔥🔥 남발

## A5. 가격 표기 규칙 (6장 전체 동일 표기 강제)
- **표기 형식은 6장 전부 똑같이.** 카드1에 "₩1,199천", 카드2에 "119만 9천원~" 처럼 다르게 쓰는 것 금지
- **권장 표기 (한국식, 직관)**: `119만 9천원~` / `74만 9천원~` / `1,199,000원~`
- **금지 표기 (즉시 의미 안 잡힘)**: `₩1,199천` / `119K` / `1.2M원`
- 가격이 등장하는 모든 카드(1번, 2번 등)는 **같은 형식**으로 작성

## A6. 가격 기준 정보 — 등장 시 반드시 동반
가격 숫자가 적히는 카드에는 다음 기준 정보를 **반드시** 함께 명시 (원문에 있을 때만):
- 1인 기준 / 2인 1실 기준 / 출발지 / 포함가 여부 / 유류할증료 포함 여부
- 원문에 없으면 임의로 "2인 1실" 같은 가정을 만들지 말 것 (Faithfulness Rule 적용)

## A7. 카드 1 (Attention) — 핵심 셀링 정보가 메인 카피보다 약하면 안 됨
- 메인 헤드라인(워드플레이/슬로건) 외에 다음 4개 핵심 정보가 **시각적으로 동등하거나 강하게** 보여야 함:
  - 목적지 / 박일정 (예: 몽골 4박5일)
  - 출발지·항공 형태 (예: 부산 직항 전세기)
  - 가격 (위 A5 표기 규칙)
  - 출발일 또는 마감 임박 배지
- 워드플레이가 의미 모호한 경우(예: "호화호특") 부제·키커로 의미 보조 ("호화롭게, 특별하게" 같은)

## A8. CTA·연락처 (마지막 카드)
- 카카오 채널/문의 정보가 명시되어야 함 — **원문이나 메타에 채널 ID가 있으면 그대로 표기**, 없으면 "카카오톡 채널 검색: 여소남" 같은 안전 표현
- "선착순 마감", "잔여석 한정" 같은 긴급성은 **원문에 그런 신호가 있을 때만** 사용

## A9. 가독성·대비 (강제 — 인스타 모바일 환경 우선)

인스타 carousel은 1080×1080 카드가 모바일에서 약 400~500px 폭으로 축소되어 표시됨. 따라서 카드 내 글자는 PC 모니터 기준이 아닌 **모바일 환경에서 읽힘** 기준으로 작성해야 함.

### A9-1. 최소 글자 크기 (절대 위반 금지)
| 요소 | 최소 px | 권장 px |
|------|---------|---------|
| 본문 / 설명 | **22px** | 24~28px |
| 캡션 / 단위 / 부가설명 | **18px** | 20~22px |
| 푸터 (yeosonam.com 등) | **16px** | 18px |
| 부제·키커 (eyebrow) | **22px** | 26~32px |
| 카드 제목 | 56px | 60~80px |
| 메인 타이틀 (카드 1) | 100px | 110~140px |
| 강조 숫자 (가격) | 80px | 90~120px |

**11~17px 같이 작은 글자 금지.** PC 디자이너 감각으로 작게 잡으면 모바일에서 읽을 수 없음.

### A9-2. 색상 대비 (WCAG AA 기준)
- 본문·중요 정보: 배경 대비 **명도 차이 4.5:1 이상**
- 큰 제목(28px+ Bold): 명도 차이 **3:1 이상**
- **rgba opacity 0.55 미만 금지** (0.4, 0.45 같은 흐린 처리 → 모바일에서 사라짐)
- 어두운 배경(딥 네이비/딥 티얼)에 회색 톤 텍스트(rgba(255,255,255,0.4))는 **금지**
  - 대신 `rgba(255,255,255,0.85)` 또는 골드/크림 포인트 컬러 사용
- 밝은 배경에 연한 컬러 텍스트도 같은 원리

### A9-3. 텍스트 겹침·잘림 절대 금지 (CSS 검증)
- `position: absolute` 다중 사용 시 z-index와 좌표 충돌 주의
- 메인 헤드라인 영역과 부제·키커는 **별도 line-height**로 충분한 수직 간격 (최소 1.4)
- font-size 큰 텍스트의 `line-height`는 1.0~1.1로 (잘림 방지)
- 한 `<div>` 안에서 텍스트 줄바꿈은 `<br>` 또는 명시적 `max-width`로 제어
- **체크**: HTML 작성 후 "두 텍스트 영역이 같은 좌표에 있는가?" 자체 검토. 있으면 재배치

### A9-4. 자율과 강제의 경계
B 영역(자율 판단)에서 컬러 팔레트와 SVG는 자유지만, **A9는 절대 우선**. 멋진 색감 위해 가독성을 희생하지 말 것. 의심되면 더 진하게, 더 크게.

